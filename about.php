				Friendship Park project was situated at Jalan Song Kuching and was completed in 2005. It officially opened to public on 18.5.2006 by Yb Datuk Seri Tengku Adnan Tengku Mansor. This unique park was the product of a close relationship between Kuching City and Kunming City of China. Due to its unique features, it gained popularity from locals and not to forget the tourists. 
                There are a few facilities in the parks which also include the pavilions were given to the Council as gifts by the Chinese agencies including Kunming Municipal Government, as symbols of friendship between China and Malaysia and between Kunming and Kuching. It is a pride and joy to the residents in MBKS for such contribution in this park. 
                These pavilions were designed, fabricated and constructed and supervised by the Chinese Landscape technicians and artisans.Timber which, in China, have lasted hundreds of years. Unfortunately they were very susceptible to our weather and humidity.
                In this regard, the Council has drawn the attention of the donor who produced these beautiful structures and appealed for their rectification in July 2007.
