				<p>
				Zheng He was an admiral and explorer from the Ming Dynasty of ancient China who visited Malaysia at a very long time ago. In the 1405, he was said to have started the worldâ€™s first biggest naval expedition, about a century ahead of Christopher Columbus.
				Zheng He escorted a contingent of 500 court maidens together with a princess called Hang Li Po. The princess was brought to marry the Sultan Mansur Syah of Malacca They eventually settled in Bukit Cina in Malacca. 
				The male and female descendants of these people from mixed marriages with the local natives are the Baba and Nyonya respectively.  
