				<h2>History of Friendship Park</h2>
                <p>
                 The establishment of a Malaysia China Friendship Park in Kuching by the Council of South Kuching City and the Kunming Municipal People's Government, Yunnan Province, China which symbolizes the flourishing friendship of the people of these two Cities and the exchange economic and cultural views.
                The Kunming Municipal People's Government made an ancient like Chinese Garden, a statue which resembles Zheng He and a hexagonal pavilion called "Huai Feng Pavilion". This is to commemorate the former glory of Admiral Zheng He.
